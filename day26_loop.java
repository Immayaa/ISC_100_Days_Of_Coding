package immayaa;

public class day26_Loop {

    public static void main(String[] args) {

        System.out.println("ini adalah for loop");
        System.out.println("1-10");
        for (int x = 0; x <= 10; x++) {
            System.out.println("immayaa : " + x);
        }

        System.out.println("10-1");

        for (int x = 10; x >= 0; x--) {
            System.out.println("immayaa : " + x);

        }
    }
}
